public abstract class SmartSubtransportStream {
  protected NObject javonetHandle;
  /** GetProperty */
  public SmartSubtransport getSmartTransport() {
    try {
      Object res = javonetHandle.<NObject>get("SmartTransport");
      if (res == null) return null;
      return ConvertToConcreteInterfaceImplementation(res);
    } catch (JavonetException _javonetException) {
      _javonetException.printStackTrace();
      return null;
    }
  }

  public SmartSubtransportStream(NObject handle) {
    this.javonetHandle = handle;
  }

  public void setJavonetHandle(NObject handle) {
    this.javonetHandle = handle;
  }
  /** Method */

  public java.lang.Integer Read(
      Stream dataStream, java.lang.Long length, AtomicReference<java.lang.Long> bytesRead) {
    try {
      java.lang.Integer res =
          javonetHandle.invoke(
              "Read",
              dataStream,
              length,
              new NOut(bytesRead, Helper.JavaToDotnetType.get(java.lang.Long.class)));
      if (res == null) return 0;
      return res;
    } catch (JavonetException _javonetException) {
      _javonetException.printStackTrace();
      return 0;
    }
  }
  /** Method */

  public java.lang.Integer Write(Stream dataStream, java.lang.Long length) {
    try {
      java.lang.Integer res = javonetHandle.invoke("Write", dataStream, length);
      if (res == null) return 0;
      return res;
    } catch (JavonetException _javonetException) {
      _javonetException.printStackTrace();
      return 0;
    }
  }

  static {
    try {
      Activation.initializeJavonet();
    } catch (java.lang.Exception e) {
      e.printStackTrace();
    }
  }
}
