public abstract class SmartSubtransport {
  protected NObject javonetHandle;

  public SmartSubtransport(NObject handle) {
    this.javonetHandle = handle;
  }

  public void setJavonetHandle(NObject handle) {
    this.javonetHandle = handle;
  }
  /** Method */

  public java.lang.Integer CertificateCheck(
      Certificate cert, java.lang.Boolean valid, java.lang.String hostname) {
    try {
      java.lang.Integer res = javonetHandle.invoke("CertificateCheck", cert, valid, hostname);
      if (res == null) return 0;
      return res;
    } catch (JavonetException _javonetException) {
      _javonetException.printStackTrace();
      return 0;
    }
  }
  /** Method */

  public java.lang.Integer AcquireCredentials(
      Credentials cred, java.lang.String user, jio.System.Type[] methods) {
    try {
      java.lang.Integer res =
          javonetHandle.invoke("AcquireCredentials", cred, user, new Object[] {methods});
      if (res == null) return 0;
      return res;
    } catch (JavonetException _javonetException) {
      _javonetException.printStackTrace();
      return 0;
    }
  }

  static {
    try {
      Activation.initializeJavonet();
    } catch (java.lang.Exception e) {
      e.printStackTrace();
    }
  }
}
