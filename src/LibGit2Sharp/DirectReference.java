public class DirectReference extends Reference
    implements IEquatable<Reference>, IBelongToARepository {
  protected NObject javonetHandle;
  /** GetProperty */
  public GitObject getTarget() {
    try {
      Object res = javonetHandle.<NObject>get("Target");
      if (res == null) return null;
      return ConvertToConcreteInterfaceImplementation(res);
    } catch (JavonetException _javonetException) {
      _javonetException.printStackTrace();
      return null;
    }
  }

  public DirectReference(NObject handle) {
    super(handle);
    this.javonetHandle = handle;
  }

  public void setJavonetHandle(NObject handle) {
    this.javonetHandle = handle;
  }
  /** Method */

  public DirectReference ResolveToDirectReference() {
    try {
      Object res = javonetHandle.invoke("ResolveToDirectReference");
      if (res == null) return null;
      return new DirectReference((NObject) res);
    } catch (JavonetException _javonetException) {
      _javonetException.printStackTrace();
      return null;
    }
  }

  static {
    try {
      Activation.initializeJavonet();
    } catch (java.lang.Exception e) {
      e.printStackTrace();
    }
  }
}
